def add_numbers(a, b):
    """
    Add two numbers and return the result.
    
    Parameters:
    a (int): The first number to be added.
    b (int): The second number to be added.
    
    Returns:
    int: The sum of a and b.
    """
    return a + b

result = add_numbers(5, 3)
print(result) # Output: 8
